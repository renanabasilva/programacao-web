#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;

    cout << "a + b = " << a + b << endl; // Soma
    cout << "a - b = " << a - b << endl; // Subtração
    cout << "a * b = " << a * b << endl; // Multiplicação
    cout << "a / b = " << a / b << endl; // Divisão
    cout << "a % b = " << a % b << endl; // Módulo

    return 0;
}
