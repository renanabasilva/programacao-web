#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 20;

    cout << "a == b: " << (a == b) << endl; // Verifica se a é igual a b
    cout << "a != b: " << (a != b) << endl; // Verifica se a é diferente de b
    cout << "a > b: " << (a > b) << endl;   // Verifica se a é maior que b
    cout << "a < b: " << (a < b) << endl;   // Verifica se a é menor que b
    cout << "a >= b: " << (a >= b) << endl; // Verifica se a é maior ou igual a b
    cout << "a <= b: " << (a <= b) << endl; // Verifica se a é menor ou igual a b

    return 0;
}
